QString emailNotFake::validate(const QVariant& _value, const QString& _fieldName)  {
    QString Email = _value.toString().trimmed();
    QString Error;
    if((Error = Validators::email().validate(_value, _fieldName)).size())
        return Error;
    if(Private::FakeDomains.contains(Email.split('@').last().toLower()))
        return _fieldName.isEmpty() ? QString("Invalid fake email: '%1'").arg(_value.toString()) : QString("'%1' must be a valid email from valid domains").arg(_fieldName);
    return {};
}


QString emailJustDomains::validate(const QVariant& _value, const QString& _fieldName)  {
    QString Email = _value.toString().trimmed();
    QString Error;
    if((Error = Validators::email().validate(_value, _fieldName)).size())
        return Error;
    if(this->Domains.contains(Email.split('@').last().toLower()) == false)
        return _fieldName.isEmpty() ? QString("Email must be just from specific domains: '%1'").arg(_value.toString()) : QString("'%1' Email must be just from specific domains").arg(_fieldName);
    return {};
}

QString emailNotDomains::validate(const QVariant& _value, const QString& _fieldName)  {
    QString Error;
    if(this->AlsoNotFake)
        Error = emailNotFake().validate(_value, _fieldName);
    else
        Error = email().validate(_value, _fieldName);
    if(Error.size())
        return Error;
    if(this->Domains.contains(_value.toString().split('@').last().toLower()) == false)
        return _fieldName.isEmpty() ? QString("Email must not be from this domain: '%1'").arg(_value.toString()) : QString("'%1' Email must not be from this domain").arg(_fieldName);
    return {};
}


